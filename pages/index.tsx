import type { NextPage } from "next";
import Head from "next/head";
import styles from "../styles/Home.module.css";
import { useState } from "react";
import ElevatorHistory from "../components/ElevatorHistory";
import { useReducer } from "react";
import { elevatorReducer } from "../reducers/ElevatorReducer";
import ElevatorDisplay from "../components/ElevatorDisplay";

const Home: NextPage = () => {
  const initialTasks = {
    doors: "closed",
    isMoving: false,
    currentFloor: 0,
    events: [],
  };

  const [elevator, dispatch] = useReducer(elevatorReducer, initialTasks);

  const decreaseFloor = () => {
    dispatch({
      type: "decrease floor",
      floor: elevator.currentFloor,
    });
  };

  const increaseFloor = () => {
    dispatch({
      type: "increase floor",
      floor: elevator.currentFloor,
    });
  };

  const openDoors = () => {
    dispatch({
      type: "open doors",
    });
  };

  const closeDoors = () => {
    dispatch({
      type: "close doors",
    });
  };

  const handleOpenDoors = () => {
    openDoors();
  };

  const handleCloseDoors = () => {
    closeDoors();
  };

  const handleIncreaseFloor = () => {
    increaseFloor();
  };

  const handleDecreaseFloor = () => {
    decreaseFloor();
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div>
        <h1>Controls</h1>
        <div>
          <input type="button" value={"Open Doors"} onClick={handleOpenDoors} />
          <input
            type="button"
            value={"Close Doors"}
            onClick={handleCloseDoors}
          />
          <input
            type="button"
            value={"Increase Floor"}
            onClick={handleIncreaseFloor}
          />
          <input
            type="button"
            value={"Decrease Floor"}
            onClick={handleDecreaseFloor}
          />
        </div>
      </div>
      <div className={styles.layoutContainer}>
        <div className={styles.leftColumn}>
          <ElevatorDisplay elevator={elevator} />
        </div>
        <div className={styles.rightColumn}>
          <ElevatorHistory history={elevator.events} />
        </div>
      </div>
    </div>
  );
};

export default Home;
